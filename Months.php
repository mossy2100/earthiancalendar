<h1>Months in the Earthian Calendar</h1>
<p>&nbsp;</p>
<h2>Number of Months per Year</h2>
<p>There are 12 months in an Earthian Calendar year.</p>
<p>There are really only two practical options for the number of months in a solar calendar. Since
  a <a href="http://en.wikipedia.org/wiki/Lunation" target="_blank">lunation</a> (the mean length
  of a <a href="http://en.wikipedia.org/wiki/Synodic_month#Synodic_month" target="_blank">synodic
    month</a>) is about 29.5 days, there are about 12.5 lunations in a year. And, because a
  calendar month is intended to be approximately the same length as lunation, yet we want each
  calendar year to have a whole number of months, this leads to calendars with either 12 or 13
  months.</p>
<p>The main advantage of a 13-month calendar is that it results in months of 28 days, which is
  equal to 4 &times; 7-day weeks. This is the basis of the <a
    href="http://en.wikipedia.org/wiki/Positivist_calendar" target="_blank">Positivist
    Calendar</a> and the <a href="http://www.newearthcalendar.com/" target="_blank">New Earth
    Calendar</a>. However, the extra 1-2 days per year remaining (28 &times; 13 = 364) still need
  to be slotted in somewhere. If there were exactly 364 days per year, then a 13-month calendar
  might be an obvious solution. However, there isn't.</p>
<p>A 12-month calendar enables the year to be easily divided into 2, 3, 4, 6 or 12 parts. This
  feature has proved itself useful in business, education, and other aspects of human society. For
  example, it is common in business to report profits for half or quarter-years, and school years
  are often divided into 2 semesters or 3 or 4 terms.</p>
<p>Being able to divide the calendar into 4 quarters is especially useful, since, on Earth, there
  are 4 seasons of approximately equal length. Because the Earthian Calendar is aligned with the
  seasonal markers, the calendar months can tells us approximately when the seasons begin and end,
  with 3 months per season.</p>
<p>Hence, a 12-month calendar is much more useful than a 13-month calendar.</p>
<p>&nbsp;</p>
<h2>Month Lengths</h2>
<p>In the Earthian Calendar, months are 30 or 31 days in length in an alternating pattern. This is
  the most balanced and easy-to-learn pattern possible, and allows the year to be neatly and <em>accurately</em>
  divided into 2, 3, 4, 6 or 12 equal parts on month boundaries. Remarkably, no major calendars
  currently in use on Earth have this feature.</p>
<h3>Comparison</h3>
<p>Within a 12-month solar calendar there are a few options for arranging month lengths, as used
  in different calendar designs over the years:</p>
<ol>
  <li>12 months of 30-31 days, alternating (Earthian, Gregorian (sort of)).</li>
  <li>6 months of 31 days and 6 months of 30 days (Iranian, Indian National).</li>
  <li>12 months of 30-31 days plus 1-2 extra days outside of the months (World).</li>
  <li>12 months of 30 days plus a 5 or 6-day &quot;mini-month&quot; (French Republican,
    Egyptian).
  </li>
  <li>Month lengths of 29-32 days aligned with 30&deg; of orbit (Persian).</li>
</ol>
<p>The problem with option 2 is that at the end of month 6 you have passed the middle of the year.
  You can't use months to divide the year accurately in half.</p>
<p>The problem with option 3 is that the 1-2 extra days outside of the months require a special
  notation (these days are written as 'W' in the World Calendar). This is an issue for computer
  programs as well as humans.</p>
<p>The problem with option 4 is that you have these 5-6 surplus days at the end of the year, not
  even a whole week. While this might be fun (you could declare this period as a global holiday
  for &quot;yearend&quot;), any benefits of having 12 equal-length months are outweighed by the
  increased complexity and weirdness. Months cannot be used to accurately divvy up the year into
  2, 3 or 4 equal parts, and you couldn't have schedules like &quot;we meet on the 3rd Thursday of
  every month&quot;, because you would always have to qualify with &quot;except for month 13&quot;.</p>
<p>Option 5 is interesting from an astronomical perspective, but if this is better then why did
  Iran change its calendar from having months of 29-32 days to months of just 30-31 days? It's
  because, as far as humans are concerned, it's more useful to have months of the same length than
  to know that Earth has moved through the same fraction of its orbit.</p>
<p>As messy as its pattern of month lengths is, the Gregorian probably uses the best &quot;system&quot;
  of month lengths out of all the calendars currently used on Earth. However, it still has some
  very obvious issues:</p>
<ol>
  <li>February having 28-29 days instead of 30-31 like all the others.</li>
  <li>The month lengths do not follow an obvious pattern, thus the need for mnemonics (&quot;30
    days hath September, what's the rest? I don't remember...&quot;)
  </li>
</ol>
<p>The Earthian Calendar fixes these issues by using a simple alternating pattern of month lengths
  that even a lobotomised monkey could learn: 30, 31, 30, 31, 30, 31, 30, 31, 30, 31, 30, 31.
  <strong>Every second month has one extra day</strong>. The only variation from this pattern is
  that the last month of the year (Pisces) has just 30 days in common years.</p>
<p>&nbsp;</p>
<h2>Month Names</h2>
<p>Calendars are based on astronomical cycles, which is why the general theme of the Earthian
  Calendar is astronomy. Thus, months are named after constellations and days of the week are
  named after planets.</p>
<p>The months have been named after the traditional 12 <em>constellations</em> of the zodiac.
  These are similar to, but different from, the <em>signs </em>of the zodiac used in astrology.
  Hence, the names &quot;Scorpius&quot; and &quot;Capricornus&quot; have been used instead of
  &quot;Scorpio&quot; and &quot;Capricorn&quot;. This has been done because:</p>
<ol type="a">
  <li>The months do not align perfectly with astrological signs and thus do not represent them.
  </li>
  <li>The calendar is intended to be scientific and astronomical more than anything else. Not
    everyone believes in astrology, yet most people believe in stars.
  </li>
</ol>
<p>Therefore, if you are interested in astrology, do not think that the Earthian month in which
  your birth date falls is necessarily equal to your star sign. While this is <em>probably</em>
  true, if you are born near the start or end of a month your actual sun sign may be different.
</p>
<p>The reason the months do not match the astrological signs of the zodiac is because each sign
  corresponds to one twelfth of Earth's orbit around the Sun (30&deg; of celestial longitude), and
  <em>not </em>one twelfth of the time it takes to complete the orbit. Earth has a slightly
  elliptical orbit and therefore does not travel at a constant pace around the Sun, which means
  the amount of time to traverse through each sign varies. When Earth is at its farthest distance
  from the Sun (aphelion) in early July, it is moving slowest, and thus the Sun takes 32 days to
  pass through the 30&deg; section of sky called Cancer. Earth is moving fastest when it is
  closest to the Sun (perihelion) in early January, and thus the Sun takes just 29 days to pass
  through the sign of Capricorn.</p>
<p>So, in a true astrological calendar the lengths of the months would thus range from 29 to 32
  days, as did the Persian Calendar. However, this results in a calendar that is less useful to
  the vast majority of its users.</p>
<p>Readers of an astronomical bent will know that, due to <a
    href="http://en.wikipedia.org/wiki/Precession_(astronomy)" target="_blank">precession of the
    equinoxes</a>, the northern spring equinox no longer occurs when the Sun is in Aries, but in
  Pisces. Thus, the names of the calendar months do not correspond to the actual position of the
  Sun on the ecliptic either; if this effect was desired then it would have been more accurate to
  start the year with Pisces.</p>
<p>Despite this (and in spite of what I've just said), the months have been named according to the
  traditional astrological constellations to which they closely align. There are several reasons
  for this:</p>
<ol type="a">
  <li>It's just simpler. Whether they believe in astrology or not, most people are already
    familiar with the association between this series of constellations and the seasons.
  </li>
  <li>There's no point making the names of the months match the Sun's position in the heavens,
    because this doesn't remain constant. Because of precession, the northern vernal equinox will
    eventually occur when the Sun is in Aquarius, then Capricornus, and so on.
  </li>
  <li>The amount of time the Sun actually spends in each constellation varies between 8 days
    (Scorpius) and 45 days (Virgo), hence months of 30-31 days do not correspond precisely with
    the constellations anyway.
  </li>
  <li>There are actually 13 constellations in the astronomical zodiac. Sol can be found in the
    constellation <em>Ophiuchus</em> between November 19 and December 17. Thus, if the months were
    really going to be named after the zodiacal constellations we would need 13 months.
  </li>
</ol>
