<h1>Calendar Background</h1>
<p>Around 1999 I adopted a very unusual hobby: calendar design. After joining the Mars Society while
  living in the Netherlands, I met a brilliant architect named Frans Blok, who has produced a
  fascinating website (the <a
    href="http://www.geocities.com/fra_nl/" target="_blank">Darian Defrost Calendar</a>) that
  combines terraforming (an interest of mine) with a well-known Mars calendar. His concept is based
  on the <a class='wiki'
    href="http://en.wikipedia.org/wiki/Darian_calendar"
    target="_blank">Darian Calendar</a>, probably the best known calendar for Mars, which was
  designed by world expert on martian time-keeping systems and creator of the <a
    href="http://www.martiana.org/mars" target="_blank">Martian Time Website</a>, Thomas Gangale, in
  1985.</p>
<p>The idea of calendar design appealed to me, combining as it does elements of maths, computing,
  astronomy, psychology, history, religion, mythology and design. For a while I enjoyed creating my
  own Mars calendar, called the Keplerian Calendar. (Hopefully this will one day be online
  again.)</p>
<p>While playing with martian calendars I studied many different calendars for Earth, some that are
  being used now, some that had been used in the past, and others that have been proposed as
  improvements. This inevitably got me thinking about what an optimal Earth calendar would look
  like, a notion that percolated in my mind for about 7 years. For most of this time I believed the
  best calendar for Earth would be lunisolar, but eventually I accepted that a solar calendar is
  better, and finally settled on the design described at this website.</p>
