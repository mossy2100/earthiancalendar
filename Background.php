<h2><strong>About </strong>me</h2>
<p>I'm a 36-year-old web programmer and space enthusiast currently living in Melbourne, Australia.
  Although I was born in Melbourne, I've lived most of my life in sunny Queensland, except for a few
  years in Europe where I lived and worked in the Netherlands, Ireland and the UK.</p>
<p>I hold a Bachelor's degree in Computer Science and Mathematics, and have taken postgraduate
  courses in Information Systems Engineering and Mechatronics Engineering. For the past 20 years
  I've worked primarily as a computer programmer, but also a computer technician, door-to-door
  salesperson, manager, teacher, design draftsperson and post-hole digger.</p>
<p>My main interest is space colonisation, and I'm a member of the <a
    href='http://www.marssociety.org' target="_blank">Mars Society</a>, the <a
    href='http://www.marshome.org' target="_blank">Mars Foundation</a>, the <a
    href='http://www.moonsociety.org' target='_blank'>Moon Society</a>, the <a
    href='http://www.space-frontier.org/' target="_blank">Space Frontier Foundation</a> and the <a
    href='http://www.nssa.com.au' target="_blank">National Space Society of Australia</a>. I've
  written <a
    href="http://shaunmoss.id.au/mars.php" target="_blank">two scientific papers about Mars</a>:
  <em>Terraforming Mars</em> and <em>Steelmaking on Mars</em>, which I presented at the
  International Mars Society Convention in Washington D.C. in 2006. These will appear in <em>On To
    Mars 3</em>, due out in 2009.</p>
<p>Over the past couple of years I have also become interested in the environment, a field I
  perceive as being strongly related to space colonisation. The similarities between ecovillages and
  space settlements led to the development of the <a href="http://ascensiontek.com" target="_blank">AscensionTek</a>
  business concept. I enjoy designing human settlements, one of my favourite ideas being an
  ambitious research facility in Antarctica called the <a
    href="http://shaunmoss.id.au/articles/ICE.php" target="_blank">Institute for Colonisation and
    Exploration</a> (ICE). My interest in planetary engineering led to the idea for the <a
    href="http://shaunmoss.id.au/blog/2008/08/global-tree-planting-program/"
    target="_blank">Global Tree-Planting Program</a>.</p>
<p>My main goal in life is to start my own global mega-corporation dedicated to technology
  development and space colonisation. Either that, or make amusing science fiction movies, since
  no-one else seems to want to or knows how. I am also trying to be a writer. It is my belief that
  the space colonisation movement will ultimately result in total environmental, emotional and
  economic healing of Earth, abundant supplies of food, water and other resources for everyone on
  our planet, the development of a benevolent world government, and peace, freedom and stable
  operating systems for all.</p>
