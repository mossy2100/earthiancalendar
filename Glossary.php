<h1>Glossary</h1>
<dl>
  <dt>aphelion</dt>
  <dd>The point in Earth's orbit where it is farthest from the Sun.</dd>
  <dt>autumnal equinox</dt>
  <dd>The point in Earth's orbit where the light part of the day is the same length as the dark, after which the nights get longer.</dd>
  <dt>calendar year</dt>
  <dd>The length of the year according to the calendar, always a whole number of days.</dd>
  <dt>common year</dt>
  <dd>A solar calendar year with 365 days.</dd>
  <dt>day</dt>
  <dd>See <em>solar day</em>.</dd>
  <dt>generation</dt>
  <dd>A period of time equal to 33 years, used as the leap year cycle in the Earthian Calendar.</dd>
  <dt>intercalary</dt>
  <dd>Refers to an extra day, week or month periodically included in a calendar year in order to maintain synchronicity with the seasonal cycle.</dd>
  <dt>leap year</dt>
  <dd>A solar calendar year of 366 days.</dd>
  <dt>lunar calendar</dt>
  <dd>A calendar that uses lunations for months, with 12 lunations per calendar year.</dd>
  <dt>lunation</dt>
  <dd>The average length of a synodic month, equal to about 29.53 days.</dd>
  <dt>lunisolar calendar</dt>
  <dd>A calendar that uses lunations for months, with either 12 or 13 lunations per calendar year.</dd>
  <dt>month</dt>
  <dd>A period equal, or approximately equal, to a lunation, with possible lengths of 28 to 31 whole days.</dd>
  <dt>perihelion</dt>
  <dd>The point in Earth's orbit where it is closest to the Sun.</dd>
  <dt>perpetual calendar</dt>
  <dd>A calendar in which weeks and months are synchronised, and thus each calendar date always occurs on the same day of the week.</dd>
  <dt>season</dt>
  <dd>The time between seasonal markers, equal to about &frac14; years.</dd>
  <dt>seasonal marker</dt>
  <dd>A solstice or an equinox.</dd>
  <dt>sol</dt>
  <dd>One martian solar day, equal to about 24 hours and 40 minutes.</dd>
  <dt>solar calendar</dt>
  <dd>A calendar that uses a fraction of a year as a month (usually <sup>1</sup>/<sub>12</sub>), and has year lengths of 365 or 366 days.</dd>
  <dt>solar day</dt>
  <dd>The time taken for Earth to rotate once on its axis and the Sun to return to the same position in the sky, equal to 24 hours.</dd>
  <dt>summer solstice</dt>
  <dd>The point in Earth's orbit where the length of the night has reached a minimum.</dd>
  <dt>synodic month</dt>
  <dd>The time for the Moon to orbit Earth and return to the same position with respect to Earth; the time between new moons.</dd>
  <dt>vernal equinox</dt>
  <dd>The point in Earth's orbit where the light part of the day is the same length as the dark, after which the nights get shorter.</dd>
  <dt>vernal equinox year</dt>
  <dd>The average period between successive northern vernal equinoxes, equal to about 365.2424 days.</dd>
  <dt>week</dt>
  <dd>A period of 7 days.</dd>
  <dt>winter solstice</dt>
  <dd>The point in Earth's orbit where the length of the night has reached a maximum.</dd>
  <dt>year</dt>
  <dd>Usually means <em>calendar year</em>.</dd>
</dl>
