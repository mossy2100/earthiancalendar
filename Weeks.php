<p>The name &quot;Luna&quot; for the Moon has been used in the Earthian days of the week, because this is the name for the Moon in many non-English languages, and is also often used in English. Personally, I prefer it. Many planets have moons, and, in my opinion, calling our moon &quot;the Moon&quot; is sort of like calling your dog &quot;the Dog&quot; instead of giving it a proper name. &quot;Lunaday&quot; corresponds to names for Monday in romance languages such as French and Italian: for example &quot;Lundi&quot; (French), &quot;Lunedi&quot; (Italian), etc.</p>
<p>To match this, I initially used &quot;Sol&quot; instead of &quot;Sun&quot;, however, &quot;Solday&quot; simply doesn't sounds as nice and &quot;sunny&quot; as &quot;Sunday&quot;.</p>
<p><em>Note:</em> I consider these names to be an entirely optional element of the calendar. Names for days of the week are firmly ingrained in our languages, and hard to change, especially since the weeks themselves haven't changed and the names refer to the same days. Also, I wouldn't want &quot;T.G.I. Friday's&quot; to have to update all their marketing. Different countries that adopt the Earthian Calendar will probably just use the same names for days of the week that they do now, which is just fine.</p>
<p>On the other hand, the names of months in the Earthian Calendar should be changed, because they refer to different times of the year; they do not line up with any existing months.</p>
<p>&nbsp;</p>
<h2>First Day of the Week</h2>
<p>The International Standards Organisation standard <a href="http://en.wikipedia.org/wiki/ISO_8601" target="_blank">ISO8601</a> specifies that weeks begin with Monday. However, this would appear to be either a European and/or business-oriented bias, since it is much more traditional to start the week with Sunday. In most world cultures, Sunday has been the first day of the week for thousands of years. Also, almost all computer languages enumerate the days of the week starting from Sunday.</p>
<p>The concept of beginning the week with Monday, on the other hand, is a relatively recent phenomenon that has emerged as a result of increasing dominance of world culture by business, and also because most people (especially in western society) now take 2 days rest per week instead of 1. Because the 2 days rest are thought of as the &quot;weekend&quot;, and the start of the work week is experienced emotionally and economically as the beginning of the week, we have come to think of the week as starting on Monday.</p>
<p>In the Earthian Calendar, weeks begin on Sunday. This choice has been made for several reasons:</p>
<ol>
  <li>To conform with the long-standing traditional pattern rather than the recent phenomenon.</li>
  <li>To preserve the definition of the <a href="http://en.wikipedia.org/wiki/Sabbath" target="_blank">Sabbath</a> from the <a href="http://en.wikipedia.org/wiki/Ten_Commandments" target="_blank">Ten Commandments</a>.</li>
</ol>
<h3>The Sabbath and the Weekend</h3>
<p>This is not a religious calendar. However, Earth is home to many religious people, whom I am hoping will like this calendar. Hence, it is necessary to understand where the Sabbath and the weekend originally came from.</p>
<p>The Sabbath, a weekly day of rest, comes from Jewish tradition and corresponds to Saturday in English. This is reflected in the names for Saturday in several languages: e.g. <em>&quot;yom Shabbat&quot; </em>in Hebrew, <em>&quot;S&aacute;vato&quot;</em> in Greek, <em>&quot;S&aacute;bado&quot;</em> in Spanish and Portuguese, and <em>&quot;Sabato&quot;</em> in Italian. The Sabbath day is the last day of the week and allocated as a day of rest after 6 days of work.</p>
<p>The original definition comes from the Ten Commandments, which were reportedly given to Moses by God as a basic set of laws for the Jewish people. The importance of the Sabbath, and its purpose, are described in the fourth commandment (Exodus 20:8-11, Deuteronomy 5:12-15):</p>
<blockquote>
  <p class="style1"> Remember the Sabbath day and keep it holy.</p>
  <p><em> For six days you shall labour and do all your work.</em></p>
  <p><em> But the seventh day is a Sabbath to the Lord your God; you shall  not do any work&mdash;you, your son or your daughter, your male or female  slave, your livestock, or the alien resident in your towns.</em></p>
  <p><em> For in six days the Lord made heaven and earth, the sea, and all  that is in them, but rested the seventh day; therefore the Lord blessed  the Sabbath day and consecrated it.</em></p>
</blockquote>
<p>So this scripture makes it quite clear that &quot;<em>the seventh day is a Sabbath</em>&quot;, meaning that the week ends on Saturday, and therefore begins on Sunday.</p>
<p>Sunday was called &quot;the Lord's Day&quot; by the Roman Catholic Church, because it was on this day that Jesus supposedly rose from the dead. Thus, Sunday was allocated for communal worship, and so began the 2-day weekend. Hooray!</p>
If the Earthian Calendar specified that Lunaday (Monday) was the first day of the week then a large section of the international religious community  would ignore it and simply adhere to the millenia-old traditional pattern of ending the week on Saturday. It would be a major barrier to adoption of the calendar in these cultures. By beginning the week with Sunday, these cultures are much more likely to accept the calendar because it won't conflict with their beliefs. Although the business culture may view the week as beginning on a Monday, clearly their feelings about this are not as strong, since many businesses still use calendars with Sunday as the first day of the week with no fuss whatsoever.
<p>As a science writer and not a theologian, it is not my intention to assert that the Ten Commandments are strictly relevant to modern society (although, frankly I think modern society would be considerably better off if they were). The point here is that we have this rather popular 3300-year-old teaching clearly describing the Sabbath, which gave us the weekend that we know and love to this day. There are no religious or scientific reasons to conflict with this, and since we already observe the Sabbath (many without realising it) and most people are already quite used to starting the week on Sunday, there seems little reason to change it. It is a mystery why ISO thought they should.</p>
