<h2>Week Numbers</h2>
<p>Week within a year are numbered from 1-52 or 53.</p>
<p>In ISO8601, week 1 of a year is defined as being the first week with 4 or more days in that year. In other words, a week is counted as belonging to the year that contains most of its days.</p>
<p>This is fairly logical, so the same rule is used in the Earthian Calendar.</p>
<p>The table below illustrates week numbering around New Year's Day for the next few years. If the year begins on a Sunday, Lunaday, Marsday or Mercuryday (as in year 0003..0006), then New Year's Day will be in week 1 of the new year. However, if the year begins on Jupiterday, Venusday or Saturnday (as in year 0001, 0002, 0007 and 0008), then New Year's Day will be in the last week of the previous year.</p>
