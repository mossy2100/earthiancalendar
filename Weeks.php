<h1>Weeks</h1>
<p>&nbsp;</p>
<h2>Week Lengths</h2>
<p>The Earthian Calendar maintains the traditional 7-day week.</p>
<p>There are few options when it comes to the length of the week. The 7-day week has been around since the time of the Babylonians, when it was a convenient period of time between market days approximately equal to the period between  phases of the moon. Since one quarter of a lunation is 29.53 days &divide; 4 = 7.38 days, this led to a constant week length of 7 days.</p>
<p>The 7-day week also features in Genesis (the first book of the Torah and the Bible), which describes how God created the universe in 7 days. This has led to its veneration and preservation by Jewish and Christian cultures. The Roman Empire used 8-day weeks, but converted to 7-day weeks as the Christian religion grew. The 7-day week continued to spread across the globe, and is now used practically everywhere on Earth.</p>
<p>There have been a few unsuccessful attempts to vary the 7-day week. For example, the French Republican Calendar, in use for about 12 years, had 10-day weeks. However these were eventually abolished because (a) workers got less rest - 1 day in every 10 instead of 1 in every 7; and (b) because it was out of synch with market days and other social cycles that stubbornly adhered to the traditional 7-day pattern.</p>
<p>Even when the Julian Calendar was replaced by the Gregorian and 10 days were deleted from October 1582, the 7-day cycle just kept rolling along as if nothing had happened.</p>
<p>A 7-day week may seem less than ideal in some ways. For example, a week with an even number of days could be evenly divided by 2, and would lend itself to alternating patterns of, for example, exercise and rest days. A 10-day week would permit decimal notation. And a pattern of 7 and 8-day weeks can be used to create a perpetual calendar.</p>
<p>However, the 7-day cycle is one of the only things that the entire planet Earth agrees on, and thus it would be foolish, not to mention rather pointless, to mess with it. If any new calendar introduced weeks that did not conform to the traditional 7-day pattern then several major religions, and in fact most of the planet, would immediately dismiss it as a mere curiousity, which would sort of defeat the purpose. After all, it was for this exact reason that the World Calendar was not adopted by the UN in 1955.</p>
<p>&nbsp;</p>
<h2>Names for Days of the Week</h2>
<p>The days of the week are named for the 7 Solar System bodies visible to the naked eye: the Sun, Luna, Mars, Mercury, Jupiter, Venus and Saturn, a naming scheme in harmony with the names currently used in most languages, and with the astronomical theme of the calendar. This naming pattern was originally developed by  Persian astrologers, and spread throughout the world to the point where most cultures outside of eastern Asia name their days of the week this way (see <a class='wiki' href="http://en.wikipedia.org/wiki/Days_of_the_week#Cross-linguistic_overview">this table</a>).</p>
<p>In days of olde, heavenly bodies were considered equivalent with gods. While the Persians may have named a day for Mars the planet, to the Romans and other pagan cultures the day was named in honour of Mars, the god of agriculture, war and chocolate. Thus, in merry England, some days of the week eventually became named, not for planets, but for the equivalent Germanic gods: Tiw, Woden, Thor and Freya (<a class='wiki' href="http://en.wikipedia.org/wiki/Days_of_the_week#Names_of_the_days" target="_blank">more about the English names for days of the week</a>).</p>
<p>However, these names are not relevant to most modern humans. There are very few followers of Tiw these days, yet there are a great many people interested in Mars. So the Earthian Calendar introduces a modern set of names for days of the week based on these 7 Solar System bodies. They conform closely to those in Latin.</p>
